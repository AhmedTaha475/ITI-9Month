var pGraph = document.getElementById("PAR");

function ChangeFont(myFont) {
  pGraph.style.fontFamily = myFont;
}
function ChangeAlign(align) {
  pGraph.style.textAlign = align;
}

function ChangeHeight(height) {
  pGraph.style.lineHeight = height;
}

function ChangeLSpace(space) {
  pGraph.style.letterSpacing = space;
}

function ChangeIndent(indent) {
  pGraph.style.textIndent = indent;
}

function ChangeTransform(transform) {
  pGraph.style.textTransform = transform;
}
function ChangeDecorate(decorate) {
  pGraph.style.textDecoration = decorate;
}

function ChangeBorder(border) {
  pGraph.style.border = border;
}

function ChangeBorderColor(color) {
  pGraph.style.borderColor = color;
}
